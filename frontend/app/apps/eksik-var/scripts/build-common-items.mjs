function pickSlugOwner(slug, names) {
  if (SLUG_PRIORITY[slug] && names.includes(SLUG_PRIORITY[slug])) {
    return SLUG_PRIORITY[slug];
  }
  return [...names].sort((a, b) => a.length - b.length)[0];
}

const slugOwners = new Map();
for (const [name, slug] of Object.entries(SLUGS)) {
  if (!slugOwners.has(slug)) slugOwners.set(slug, []);
  slugOwners.get(slug).push(name);
}

const winnerBySlug = Object.fromEntries(
  [...slugOwners.entries()].map(([slug, names]) => [slug, pickSlugOwner(slug, names)])
);

const original = JSON.parse(readFileSync(join(root, "common_items.json"), "utf8"));
const missing = [];

const output = original.map((group) => ({
  category: group.category,
  items: group.items.map((entry) => {
    const name = typeof entry === "string" ? entry : entry.name;
    const slug = SLUGS[name];
    if (!slug || winnerBySlug[slug] !== name) {
      return { name };
    }

    const kind = KIND_BY_CATEGORY[group.category];
    const img =
      getCatalogItemImage(slug, kind ? { kind } : {}) ?? getCatalogItemImage(slug);
    if (!img) {
      missing.push(`${name} -> ${slug}`);
      return { name };
    }

    return { name, slug: img.slug, atlasCategory: img.category };
  }),
}));

if (missing.length) {
  console.warn(`Missing atlas images (${missing.length}):`, missing.slice(0, 20).join(", "));
}

writeFileSync(join(root, "common_items.json"), `${JSON.stringify(output, null, 2)}\n`);
